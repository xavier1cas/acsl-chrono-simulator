function export_ref_traj_and_yaml(traj, data)
% =========================================================================
% Function: export_ref_traj_and_yaml
%
% Purpose:
%   Exports trajectory data from 'traj' into a CSV file and copies the YAML
%   file from a standard input directory to the corresponding output folder.
%
% Inputs:
%   traj - Struct containing trajectory data variables (NED, quaternion, etc.).
%   data - Struct containing at least the trajectory 'name' field.
% =========================================================================

% -------------------------------------------------------------------------
% 1. Build the reference trajectory table (36 fields, see header names below)
% -------------------------------------------------------------------------
ref_traj_table = table( ...
    traj.time_ned(:), ...
    traj.positions_ned(:,1), ...
    traj.positions_ned(:,2), ...
    traj.positions_ned(:,3), ...
    traj.velocities_ned(:,1), ...
    traj.velocities_ned(:,2), ...
    traj.velocities_ned(:,3), ...
    traj.accelerations_ned(:,1), ...
    traj.accelerations_ned(:,2), ...
    traj.accelerations_ned(:,3), ...
    traj.euler_ned(:,1), ...
    traj.euler_ned(:,2), ...
    traj.euler_ned(:,3), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    traj.quats_ned(:,1), ...
    traj.quats_ned(:,2), ...
    traj.quats_ned(:,3), ...
    traj.quats_ned(:,4), ...
    traj.quats_dot_ned(:,1), ...
    traj.quats_dot_ned(:,2), ...
    traj.quats_dot_ned(:,3), ...
    traj.quats_dot_ned(:,4), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    zeros(numel(traj.time_ned),1), ...
    traj.angular_vels_ned(:,1), ...
    traj.angular_vels_ned(:,2), ...
    traj.angular_vels_ned(:,3), ...
    traj.angular_accs_ned(:,1), ...
    traj.angular_accs_ned(:,2), ...
    traj.angular_accs_ned(:,3), ...
    'VariableNames', { ...
        'time', ...
        'pos_x', 'pos_y', 'pos_z', ...
        'vel_x', 'vel_y', 'vel_z', ...
        'acc_x', 'acc_y', 'acc_z', ...
        'euler_roll', 'euler_pitch', 'euler_yaw', ...
        'euler_rate_r', 'euler_rate_p', 'euler_rate_y', ...
        'euler_acc_r', 'euler_acc_p', 'euler_acc_y', ...
        'quat_w', 'quat_x', 'quat_y', 'quat_z', ...
        'quat_rate_w', 'quat_rate_x', 'quat_rate_y', 'quat_rate_z', ...
        'quat_acc_w', 'quat_acc_x', 'quat_acc_y', 'quat_acc_z', ...
        'ang_vel_x', 'ang_vel_y', 'ang_vel_z', ...
        'ang_acc_x', 'ang_acc_y', 'ang_acc_z' ...
    });

% -------------------------------------------------------------------------
% 2. Construct output folder and CSV filename, create folder if needed
% -------------------------------------------------------------------------
output_folder = fullfile('output', data.name);
if ~exist(output_folder, 'dir')
    mkdir(output_folder);
end
csv_filename = fullfile(output_folder, [data.name, '.csv']);
writetable(ref_traj_table, csv_filename);

% -------------------------------------------------------------------------
% 3. Copy YAML file from input to output folder with matching base name
% (Assumes standard input location and file name structure: 'input/datafile.yaml')
% -------------------------------------------------------------------------
source_yaml = fullfile('input', 'datafile.yaml');
destination_yaml = fullfile(output_folder, [data.name, '.yaml']);
copyfile(source_yaml, destination_yaml);

% -------------------------------------------------------------------------
% 4. Confirmation messages
% -------------------------------------------------------------------------
fprintf('Reference trajectory CSV exported to: %s\n', csv_filename);
fprintf('YAML file copied to: %s\n', destination_yaml);
end
